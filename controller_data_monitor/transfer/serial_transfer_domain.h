#ifndef SERIAL_TRANSFER_DOMAIN_H
#define SERIAL_TRANSFER_DOMAIN_H

namespace serial {

/**
 Скорость передачи (бодрейт)
 */
enum class BaudRate {
    BR_110    = 110,      // 110 бод
    BR_300    = 300,      // 300 бод
    BR_600    = 600,      // 600 бод
    BR_1200   = 1200,     // 1200 бод
    BR_2400   = 2400,     // 2400 бод
    BR_4800   = 4800,     // 4800 бод
    BR_9600   = 9600,     // 9600 бод (наиболее распространённый)
    BR_14400  = 14400,    // 14400 бод
    BR_19200  = 19200,    // 19200 бод
    BR_38400  = 38400,    // 38400 бод
    BR_57600  = 57600,    // 57600 бод
    BR_115200 = 115200,   // 115200 бод
    BR_230400 = 230400,   // 230400 бод
    BR_460800 = 460800,   // 460800 бод
    BR_921600 = 921600    // 921600 бод
};

/**
  Количество бит данных
 */
enum class DataBits {
    FIVE  = 5,   // 5 бит данных (редко используется)
    SIX   = 6,   // 6 бит данных
    SEVEN = 7,   // 7 бит данных
    EIGHT = 8    // 8 бит данных (наиболее распространённый)
};

/**
  Контроль чётности
 */
enum class Parity {
    NONE  = 0,  // Нет проверки чётности
    ODD   = 1,  // Нечётный (Odd)
    EVEN  = 2   // Чётный (Even)
};

/**
  Количество стоп-битов
 */
enum class StopBits {
    ONE      = 1,   // 1 стоп-бит (наиболее распространённый)
    TWO      = 2,   // 2 стоп-бита
    ONE_HALF = 3    // 1.5 стоп-бита (редко используется)
};

/**
  Управление потоком (Flow Control)
 */
enum class FlowControl {
    NONE,       // Нет управления потоком
    HARDWARE,   // Аппаратное (RTS/CTS)
    SOFTWARE    // Программное (XON/XOFF)
};

} // namespace serial

#endif // SERIAL_TRANSFER_DOMAIN_H
